window.onscroll = function() {scrollFunction()};
document.getElementById("navbar").style.top = "-" +  document.getElementById("navbar").offsetHeight + "vh"
function scrollFunction() {
  let navbarh = "-" +  document.getElementById("navbar").offsetHeight + "vh";
  if (document.body.scrollTop > document.body.clientHeight || document.documentElement.scrollTop > document.documentElement.clientHeight) {
    document.getElementById("navbar").style.top = "3vh";
  } else {
    document.getElementById("navbar").style.top = navbarh;
  }
}



function showtxt1() {
  var text1 = document.getElementById("text1");
  var tuks = document.getElementById("tuks");
  if (tuks.style.display === "none") {
    tuks.style.display = "inline";
    text1.style.height = "0%";
  } else {
    tuks.style.display = "none";
    text1.style.height = text1.scrollHeight + "px";
  }
  
}

